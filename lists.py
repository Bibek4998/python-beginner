# Example of lists in python>>>>>>
# In the positive indexing the indexing starts from the index 0>>>>>>>
print("Displaying all the list items: ")
fruits=["Apple","Mangoes","Papaya","Banana"]
print(fruits)

# Accessing the list items>>>>
arr=["Bibek","Aayush","Ankit","Sudip","Khadga","Yuvraj"]
print("Displaying the list items (indexed):")
print(arr[1])

# Negative indexing>>>
print("Negative indexing....")
print(arr[-2]) #In the negative indexing the indexing starts from the index 1>>>>

# Range in indexing>>>>>
print("Range in lists arr[2:5]....")
print(arr[2:5]) #This will display the range from index 2-5(index 3,4 and 5 will be printed..)
print("Printing the list from the index to end arr[2:]....")
print(arr[2:]) #This will print the value from index 3 to end of the list...

# Negative indexing in the range....
print("Negative indexing in the range...")
print(arr[-4:-1])

